            Скользящие средние (Moving Averages) являются одним из самых популярных индикаторов. 
            Простая скользящая средняя (SMA) рассчитывается как среднее арифметическое цен за 
            определенный период. Экспоненциальная скользящая средняя (EMA) придает больше веса 
