        <h2>Технический анализ торговых пар</h2>
        
        <p>
            Технический анализ — это метод оценки торговых пар путем анализа статистических тенденций, 
            собранных из торговой активности, таких как движение цен и объемы. В отличие от 
            фундаментального анализа, который пытается оценить ценность актива на основе финансовых 
            результатов и экономических событий, технический анализ сосредоточен на изучении графиков 
            цен и использовании различных технических индикаторов для прогнозирования будущих движений цен.
        </p>

        <p>
            Одним из основных инструментов технического анализа являются свечные графики. Каждая свеча 
            представляет собой определенный временной период (например, 1 час, 4 часа или 1 день) и 
            показывает цену открытия, максимальную цену, минимальную цену и цену закрытия за этот период. 
            Зеленые свечи указывают на рост цены (бычий тренд), а красные — на падение (медвежий тренд). 
            Изучение свечных паттернов, таких как "молот", "падающая звезда", "поглощение" и "доджи", 
            помогает трейдерам определять разворотные точки на рынке.
        </p>

